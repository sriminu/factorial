void main() {
  var size = 'm';
  switch(size){
    case 'xs':
      print('my shirt size is xs');
      break;
    case 's':
      print('my shirt size is small');
      break;
    case "xl":
      print('my shirt size is xl');
      break;
    case "m":
      print('my shirt size is m');
      break;
    default:
      print('no size found');
  }
}