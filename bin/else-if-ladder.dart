void main () {
  var month = 'april';
  if (month == 'january') {
    print('january');
  } else if (month == "febuary") {
    print('february');
  } else if (month == 'march') {
    print('march');
  } else if (month == 'december') {
    print('december');
  } else {
    print('none of the above');
  }
}